// switch case
let weekDay = 7

switch (weekDay) {
    case 1:
        console.log("Lunes")
        break;
    case 2:
        console.log("Martes")
        break;
    case 3:
        console.log("Miércoles")
        break;
    case 4:
        console.log("Jueves")
        break;
    default:
        console.log("No es un día de la semana")
        break;
}